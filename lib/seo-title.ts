/**
 * Returns a short university name (≤25 chars) suitable for page titles.
 * Falls back to a cleaned/truncated version of the full name, or the abbreviation.
 */
export function getTitleName(id: string, name: string, abbr: string): string {
  if (TITLE_NAME[id]) return TITLE_NAME[id]

  // Clean: strip trailing "Online" and parenthetical suffixes
  const cleaned = name
    .replace(/\s*\(.*?\)\s*/g, '')
    .replace(/\s+[Oo]nline\s*$/, '')
    .trim()

  if (cleaned.length <= 25) return cleaned

  // Truncate to 25 chars at a word boundary
  const truncated = cleaned.substring(0, 25).replace(/\s+\S*$/, '').trim()
  if (truncated.length >= 10) return truncated

  // Last resort: use abbreviation
  return abbr || cleaned.substring(0, 25)
}

