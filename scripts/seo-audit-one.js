#!/usr/bin/env node
const fs = require('fs')
const path = require('path')

const slug = process.argv[2] || 'mba-operations-management-career-2026'
const src = fs.readFileSync(path.join(__dirname, '..', 'lib', 'blog.ts'), 'utf8').replace(/\r\n/g, '\n')

const i = src.indexOf(`slug: '${slug}'`)
if (i < 0) { console.error('Not found:', slug); process.exit(1) }
const end = src.indexOf('\n  {\n', i)
const block = src.slice(i, end)

const get = re => (block.match(re) || [])[1] || ''

const title    = get(/^\s*title: '((?:[^'\\]|\\.)*)'/m)
const seoTitle = get(/^\s*seoTitle: '((?:[^'\\]|\\.)*)'/m)
const meta     = get(/^\s*metaDescription: '((?:[^'\\]|\\.)*)'/m)
const kw       = get(/^\s*targetKeyword: '([^']+)'/m)
const content  = (block.match(/content: `([\s\S]*?)`,?\s*\n  \},?/) || [])[1] || ''

console.log('=== SEO META AUDIT ===')
console.log('Slug              :', slug, '(' + slug.length + ' chars)')
console.log('Target keyword    :', kw)
console.log('Title             :', title)
console.log('  length          :', title.length, 'chars', title.length <= 60 ? '✓' : '⚠ over 60')
console.log('  contains kw     :', title.toLowerCase().includes(kw.toLowerCase()) ? '✓' : '✗')
console.log('SEO Title (tab)   :', seoTitle)
console.log('  length          :', seoTitle.length, 'chars', seoTitle.length <= 60 ? '✓' : '⚠ over 60')
console.log('Meta description  :', meta)
console.log('  length          :', meta.length, 'chars', (meta.length >= 150 && meta.length <= 160) ? '✓' : meta.length < 150 ? '⚠ under 150' : '⚠ over 160')
console.log('  contains kw     :', meta.toLowerCase().includes(kw.toLowerCase()) ? '✓' : '✗')

console.log()
console.log('=== HEADING STRUCTURE ===')
const h1Count = (content.match(/<h1/gi) || []).length
const h2s = [...content.matchAll(/<h2[^>]*>([\s\S]*?)<\/h2>/gi)].map(m => m[1].replace(/<[^>]+>/g, '').trim())
const h3s = [...content.matchAll(/<h3[^>]*>([\s\S]*?)<\/h3>/gi)].map(m => m[1].replace(/<[^>]+>/g, '').trim())
console.log('H1 in body        :', h1Count, h1Count === 0 ? '✓ (renderer adds H1 from title)' : '✗ MUST be 0')
console.log('H2 count          :', h2s.length, h2s.length >= 4 ? '✓' : '⚠ < 4')
h2s.forEach((h, i) => console.log('  H2.' + (i + 1) + ':', h))
console.log('H3 count          :', h3s.length)
h3s.forEach((h, i) => console.log('  H3.' + (i + 1) + ':', h))

const kwInH2 = h2s.filter(h => h.toLowerCase().includes(kw.toLowerCase()) || h.toLowerCase().includes('operations')).length
console.log('H2s mentioning kw/topic:', kwInH2, kwInH2 >= 2 ? '✓' : '⚠ < 2')

console.log()
console.log('=== KEYWORD USAGE ===')
const plain = content.replace(/<[^>]+>/g, ' ').toLowerCase()
const totalWords = plain.split(/\s+/).filter(Boolean).length
const kwRe = new RegExp(kw.replace(/[.*+?^${}()|[\]\\]/g, '\\$&'), 'gi')
const kwMatches = (plain.match(kwRe) || []).length
console.log('Total words       :', totalWords)
console.log('Keyword mentions  :', kwMatches)
console.log('Density           :', ((kwMatches / totalWords) * 100).toFixed(2) + '%', '(target: 0.5-2.0%)')

const first100 = plain.split(/\s+/).filter(Boolean).slice(0, 100).join(' ')
console.log('Kw in first 100 wds:', first100.includes(kw.toLowerCase()) ? '✓' : '⚠ broader topic present but exact kw not')

const ops = (plain.match(/operations/g) || []).length
console.log('"operations" total:', ops)

console.log()
console.log('=== FAQ ===')
const faqsMatch = block.match(/faqs: \[([\s\S]*?)\],/)
const faqArr = faqsMatch ? [...faqsMatch[1].matchAll(/\{\s*q:/g)] : []
console.log('FAQ count         :', faqArr.length, faqArr.length >= 5 ? '✓' : '⚠ < 5')

console.log()
console.log('=== STRUCTURED DATA (auto-generated by renderer) ===')
console.log('FAQPage schema    : ✓ generated from faqs array (app/blog/[slug]/page.tsx)')
console.log('Article schema    : ✓ generated (uses heroImage if present)')
console.log('WebPage schema    : ✓ generated')
console.log('BreadcrumbList    : ✓ generated')
console.log('OG image          :', /heroImage:/.test(block) ? '✓ uses heroImage' : 'fallback og.webp')

console.log()
console.log('=== INTERNAL LINKS ===')
const links = [...content.matchAll(/href="(\/[^"#]*)"/g)].map(m => m[1])
const uniqLinks = [...new Set(links)]
console.log('Total internal hrefs:', links.length)
console.log('Unique             :', uniqLinks.length, uniqLinks.length >= 5 ? '✓' : '⚠ < 5')
uniqLinks.forEach(l => console.log('  →', l))
